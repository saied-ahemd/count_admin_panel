    public function confirm( $id)
    {
        $order=Order::find($id);

        $order->update([
            'status' =>1,
        ]);

        return redirect()->route('orders.index')->with('mess','the order has benn confirmed');
    }

    //pending function to pende the order

    public function pending( $id)
    {
        $order=Order::find($id);
        
